<h1>Bienvenido a Sabonsai, la mejor tienda on-line de bonsais</h1>
    <p>En Sabonsai clasificamos nuestros bonsais en distintas categorias dependiendo del tamaño.</p>
    <h2>Mame</h2>
    <p>Se trata de un bonsái que mide ente 5 y 15cm su definición es simplemente "todo bonsái que entre en la palma de una mano" y como los shito necesitan de más frecuencia de riego y abono ya que su maceta igualmente es de tamaño pequeño.  </p>
    <h2>Shohin</h2>
    <p>Se trata de un bonsái de menos de 25cm. El término japonés Shohin significa “algo pequeño”, por lo que en el argot del Bonsái denota que el tamaño del árbol es incluso menor que el de un Bonsái normal, aunque según los expertos creen que se puede llamar Shohin a cualquier árbol que pueda ser mantenido en el aire con una sola mano. </p>
    <h2>Komono</h2>
    <p>Se trata de la categoría más popular entre los aficionados, es de talla media de unos 15 cm a 40 cm . Este tipo de Bonsái ya no es tan delicado, como los de menor tamaño. </p>
    <h2>Chumono</h2>
    <p>Con esta denominación se distinguen a aquellos árboles cuyo crecimiento tiene una altura mínima de 30 centímetros y una máxima de 60. Se levantan con una mano. Como en el tamaño anterior es de los más populares entre los aficionados.</p>
    <h2>Omono</h2>
    <p>Se trata de un bonsái de entre 60cm y 120cm, forman parte del grupo de grandes bonsáis, en los que se necesitan de dos personas para su transporte. </p>
    <h2>Hachi-Uye</h2>
    <p>Este último es realmente es un árbol modelado como bonsái de más de 130 centímetros.</p>
